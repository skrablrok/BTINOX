document.getElementById("readmore-section").addEventListener("toggle", function() {
    const imgContainer = document.getElementById("dynamic-image");

    if (this.open) {
        imgContainer.innerHTML = `<img src="prehrambena/nacrtovanje_1.jpeg" alt="Načrtovanje in inženiring">`;
    } else {
        imgContainer.innerHTML = ''; // počisti slike
    }
});










